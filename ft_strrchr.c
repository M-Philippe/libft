#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	int		i;

	p = NULL;
	i = 0;
	while (s[i])
	{
		if (c == s[i])
			p = (char *)&s[i];
		i++;
	}
	if (c == s[i])
		p = (char *)&s[i];
	return (p);
}
